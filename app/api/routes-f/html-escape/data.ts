// Reverse mapping for unescaping
export const reverseNamedEntities: { [key: string]: string } = {};
Object.entries(namedEntities).forEach(([name, char]) => {
  reverseNamedEntities[char] = name;
});

export const escapeHtml = (input: string): string => {
  return input
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;'); // Use numeric for apostrophe
};

export const unescapeHtml = (input: string): string => {
  return input
    // Handle numeric entities (#65; and #x41;)
    .replace(/&#(\d+);/g, (match, dec) => {
      const code = parseInt(dec, 10);
      return String.fromCharCode(code);
    })
    .replace(/&#x([0-9a-fA-F]+);/g, (match, hex) => {
      const code = parseInt(hex, 16);
      return String.fromCharCode(code);
    })
    // Handle named entities
    .replace(/&([a-zA-Z]+);/g, (match, name) => {
      return namedEntities[name] || match;
    });
};
